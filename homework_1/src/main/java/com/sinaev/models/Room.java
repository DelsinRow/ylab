package com.sinaev.models;

public class Room {
    private String name;
    private RoomType type;

    public Room(String name, RoomType type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public RoomType getType() {
        return type;
    }

    public void setType(RoomType type) {
        this.type = type;
    }


}
